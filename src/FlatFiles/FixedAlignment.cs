using System;

namespace FlatFiles
{
    /// <summary>
    /// Gets the alignment of a fixed width column.
    /// </summary>
    public enum FixedAlignment
    {
        /// <summary>
        /// The content in the column is left aligned.
        /// </summary>
        LeftAligned,

        /// <summary>
        /// The content in the column is right aligned.
        /// </summary>
        RightAligned
    }
}
